    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        try {
        TableModel model = tblBitacora.getModel();
        JFrame parentFrame = new JFrame();

        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Libro de Excel", "xls");
        fileChooser.setFileFilter(filter);
        fileChooser.setDialogTitle("Seleccione donde desea guardar el archivo" );   
        String path;
        int userSelection = fileChooser.showSaveDialog(parentFrame);
        File file;
        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            file = new File(fileToSave.getAbsolutePath()+".xls");
            path=(fileToSave.getAbsolutePath()+".xls");
            
        FileWriter output = new FileWriter(file);


        for(int i = 0; i <model.getColumnCount(); i++){
            output.write(model.getColumnName(i) + "\t");
        }

        output.write("\n");

        for(int k=0;k<model.getRowCount();k++) {
            for(int j=0;j<model.getColumnCount();j++) {
                output.write(model.getValueAt(k,j).toString()+"\t");
            }
            output.write("\n");

    }

        output.close();
        JOptionPane.showMessageDialog(null,"Se exportó correctamente", "Éxito", JOptionPane.INFORMATION_MESSAGE );
        Runtime.getRuntime().exec("cmd /c start "+path); 
    }else{JOptionPane.showMessageDialog(null,"Error al exportar", "Error", JOptionPane.INFORMATION_MESSAGE );
        }
   }
    catch(Exception e) {
        e.printStackTrace();JOptionPane.showMessageDialog(null,"Error al exportar", "Error", JOptionPane.INFORMATION_MESSAGE );
        
    }
    }//GEN-LAST:event_jButton1MouseClicked

