    public void llenaTabla(){
        //DefaultTableModel model = new DefaultTableModel();
        //tblBitacora = new JTable(model);
        DefaultTableModel model = (DefaultTableModel) tblBitacora.getModel();
        int rowCount = model.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i=0; i < rowCount; i++) {
            model.removeRow(0);
        }
        String[] bitacora=new auth().bitacora();
        int cual=bitacora.length-3,cantidad=0;
        String dia=(bitacora[cual].split("-"))[2];
        String mes=(bitacora[cual].split("-"))[1];
        while(dia.equals(new auth().getDia())&&mes.equals(new auth().getMes())){
            cantidad++;
            cual--;
            if(cual>0){
              dia=(bitacora[cual].split("-"))[2];
              mes=(bitacora[cual].split("-"))[1];  
            }else{
                dia="";
                mes="";
            }            
        }
        files=new int[cantidad];
        int cuantos=0;
        if(cantidad>0){
            for(int i=bitacora.length-3;i>(bitacora.length-3)-cantidad;i--){
                String[] datos=new auth().returnRow(bitacora[i]).split("&-&");
                    model.addRow(new Object[]{datos[1], datos[2], datos[5],datos[4],datos[6]});
                    files[cuantos]=i;
                    cuantos++;
            }
        }
        //model.addRow(new Object[]{"30/01/2015", "10:30:20", "Kolbi","87878787","1000"});
    }
    
